@dataclass(frozen=True)
class LexiconEntry:
    lookup_key: str
    entry_type: str
    article: str
    nom_sg: str
    gen_sg: str
    gen_sg_ending: str
    lsj_headword: str
    aliases: tuple[str, ...] = ()
    parent_key: str = ""
    status: str = "curated"
    notes: str = ""

    def tsv_row(self, occurrence_count: int, dataset_keys: list[str]) -> dict[str, str]:
        return {
            "lookup_key": self.lookup_key,
            "entry_type": self.entry_type,
            "article": self.article,
            "nom_sg": self.nom_sg,
            "gen_sg": self.gen_sg,
            "gen_sg_ending": self.gen_sg_ending,
            "lsj_headword": self.lsj_headword,
            "display_entry": display_entry(self),
            "parent_key": self.parent_key,
            "occurrence_count": str(occurrence_count),
            "dataset_keys": "; ".join(dataset_keys),
            "status": self.status,
            "notes": self.notes,
        }


@dataclass
class MatchedOccurrence:
    recipe_id: str
    dataset_key: str
    ingredient_id: str
    source_span: str
    normalized_label: str
    base_label: str
    certainty: str
    notes: list[str] = field(default_factory=list)


