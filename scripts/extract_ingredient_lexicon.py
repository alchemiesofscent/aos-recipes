def nfc(value: str) -> str:
    return unicodedata.normalize("NFC", value or "")


def strip_diacritics(value: str) -> str:
    decomposed = unicodedata.normalize("NFD", value)
    return "".join(ch for ch in decomposed if unicodedata.category(ch) != "Mn")


def match_key(value: str) -> str:
    plain = strip_diacritics(nfc(value).lower()).replace("ς", "σ")
    plain = re.sub(r"[^\w𐆄]+", " ", plain, flags=re.UNICODE)
    return re.sub(r"\s+", " ", plain).strip()


def alias_keys(entry_value: LexiconEntry) -> set[str]:
    values = {entry_value.lookup_key, entry_value.nom_sg, entry_value.gen_sg, entry_value.lsj_headword}
    values.update(entry_value.aliases)
    return {match_key(value) for value in values if value}


def display_entry(entry_value: LexiconEntry) -> str:
    prefix = f"{entry_value.article} " if entry_value.article else ""
    return f"{prefix}{entry_value.nom_sg}, {entry_value.gen_sg_ending}"


ALIAS_INDEX: tuple[tuple[str, str], ...] = tuple(
    sorted(
        (
            (alias, lookup_key)
            for lookup_key, item in CURATED_ENTRIES.items()
            for alias in alias_keys(item)
            if alias
        ),
        key=lambda item: (-len(item[0]), item[1]),
    )
)


def aliases_for_label(label: str) -> tuple[str, ...]:
    exact = EXACT_LABEL_TO_KEYS.get(nfc(label))
    if exact:
        return exact

    label_key = f" {match_key(label)} "
    matches: list[str] = []
    for alias, lookup_key in ALIAS_INDEX:
        if f" {alias} " in label_key and lookup_key not in matches:
            matches.append(lookup_key)
    return tuple(matches)


def load_recipe_payload() -> dict[str, Any]:
    return json.loads(RECIPES_JSON.read_text(encoding="utf-8"))


def iter_ingredient_occurrences(recipes: list[dict[str, Any]]):
    for recipe in recipes:
        for ingredient in recipe.get("ingredients") or []:
            yield recipe, ingredient


def build_lexicon() -> tuple[list[dict[str, str]], dict[str, Any], list[str]]:
    payload = load_recipe_payload()
    recipes = payload.get("recipes") or []
    if len(recipes) != EXPECTED_RECIPE_COUNT:
        raise ValueError(f"Expected {EXPECTED_RECIPE_COUNT} recipes, found {len(recipes)}")

    matched: dict[str, list[MatchedOccurrence]] = defaultdict(list)
    variants: dict[str, Counter[str]] = defaultdict(Counter)
    unmapped: Counter[str] = Counter()
    ingredient_count = 0

    for recipe, ingredient in iter_ingredient_occurrences(recipes):
        ingredient_count += 1
        label = nfc(ingredient.get("normalized_label") or ingredient.get("base_label") or ingredient.get("source_span") or "")
        keys = aliases_for_label(label)
        if not keys:
            unmapped[label] += 1
            continue
        occurrence = MatchedOccurrence(
            recipe_id=recipe.get("recipe_id", ""),
            dataset_key=recipe.get("dataset_key", ""),
            ingredient_id=ingredient.get("ingredient_id", ""),
            source_span=ingredient.get("source_span", ""),
            normalized_label=label,
            base_label=ingredient.get("base_label", ""),
            certainty=ingredient.get("certainty", ""),
            notes=list(ingredient.get("notes") or []),
        )
        for key in keys:
            matched[key].append(occurrence)
            variants[key][label] += 1

    if ingredient_count != EXPECTED_INGREDIENT_OCCURRENCE_COUNT:
        raise ValueError(
            f"Expected {EXPECTED_INGREDIENT_OCCURRENCE_COUNT} ingredient occurrences, found {ingredient_count}"
        )

    rows: list[dict[str, str]] = []
    entries_json: list[dict[str, Any]] = []
    for lookup_key in sorted(matched, key=match_key):
        item = CURATED_ENTRIES[lookup_key]
        occurrences = matched[lookup_key]
        dataset_keys = sorted({occ.dataset_key for occ in occurrences})
        rows.append(item.tsv_row(len(occurrences), dataset_keys))
        entries_json.append(
            {
                **item.tsv_row(len(occurrences), dataset_keys),
                "occurrence_count": len(occurrences),
                "dataset_keys": dataset_keys,
                "variants": [
                    {"normalized_label": label, "count": count}
                    for label, count in sorted(variants[lookup_key].items(), key=lambda item: match_key(item[0]))
                ],
                "occurrences": [
                    {
                        "recipe_id": occ.recipe_id,
                        "dataset_key": occ.dataset_key,
                        "ingredient_id": occ.ingredient_id,
                        "source_span": occ.source_span,
                        "normalized_label": occ.normalized_label,
                        "base_label": occ.base_label,
                        "certainty": occ.certainty,
                        "notes": occ.notes,
                    }
                    for occ in occurrences
                ],
            }
        )

    json_payload = {
        "metadata": {
            "generated_at": datetime.now(UTC).strftime("%Y-%m-%dT%H:%M:%SZ"),
            "source": str(RECIPES_JSON.relative_to(ROOT)),
            "recipe_count": len(recipes),
            "ingredient_occurrence_count": ingredient_count,
            "entry_count": len(rows),
            "unmapped_label_count": sum(unmapped.values()),
            "unmapped_labels": [
                {"normalized_label": label, "count": count}
                for label, count in sorted(unmapped.items(), key=lambda item: match_key(item[0]))
            ],
            "lsj_headword_policy": (
                "lsj_headword is an explicit curated LSJ citation-form join key. "
                "It is not inferred from the recipe normalized_label."
            ),
        },
        "entries": entries_json,
    }
    return rows, json_payload, list(unmapped)


def write_outputs(rows: list[dict[str, str]], json_payload: dict[str, Any]) -> None:
    with OUTPUT_TSV.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=TSV_FIELDS, delimiter="\t", lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)
    OUTPUT_JSON.write_text(json.dumps(json_payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def read_tsv(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle, delimiter="\t"))


def check_outputs(rows: list[dict[str, str]], json_payload: dict[str, Any], *, strict: bool) -> list[str]:
    errors: list[str] = []
    if json_payload["metadata"]["unmapped_label_count"]:
        errors.append(f"{json_payload['metadata']['unmapped_label_count']} ingredient labels are unmapped")

    seen: set[str] = set()
    for row in rows:
        key = row["lookup_key"]
        if key in seen:
            errors.append(f"Duplicate lookup_key: {key}")
        seen.add(key)

        for field_name in ("article", "nom_sg", "gen_sg", "gen_sg_ending", "lsj_headword"):
            if row["entry_type"] != "modifier" and not row[field_name]:
                errors.append(f"{key}: missing {field_name}")
        if row["entry_type"] == "named_subtype" and not row["parent_key"]:
            errors.append(f"{key}: named_subtype missing parent_key")
        if strict and row["status"] != "curated":
            errors.append(f"{key}: status is {row['status']}, not curated")

    if OUTPUT_TSV.exists() and read_tsv(OUTPUT_TSV) != rows:
        errors.append(f"{OUTPUT_TSV.relative_to(ROOT)} is stale; rerun without --check")
    if OUTPUT_JSON.exists():
        existing = json.loads(OUTPUT_JSON.read_text(encoding="utf-8"))
        comparable_existing = {**existing, "metadata": {**existing.get("metadata", {}), "generated_at": None}}
        comparable_new = {**json_payload, "metadata": {**json_payload.get("metadata", {}), "generated_at": None}}
        if comparable_existing != comparable_new:
            errors.append(f"{OUTPUT_JSON.relative_to(ROOT)} is stale; rerun without --check")
    return errors


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="Verify generated artifacts are current.")
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Also require every emitted row to have status=curated.",
    )
    args = parser.parse_args(argv)

    rows, json_payload, unmapped = build_lexicon()
    if args.check:
        errors = check_outputs(rows, json_payload, strict=args.strict)
        if errors:
            for error in errors:
                print(f"FAIL: {error}", file=sys.stderr)
            return 1
        print(
            f"PASS: {len(rows)} ingredient lexicon entries; "
            f"{json_payload['metadata']['ingredient_occurrence_count']} source occurrences; "
            f"{len(unmapped)} unmapped labels"
        )
        return 0

    write_outputs(rows, json_payload)
    print(
        f"Wrote {OUTPUT_TSV.relative_to(ROOT)} and {OUTPUT_JSON.relative_to(ROOT)} "
        f"({len(rows)} entries; {json_payload['metadata']['ingredient_occurrence_count']} source occurrences; "
        f"{len(unmapped)} unmapped labels)"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
